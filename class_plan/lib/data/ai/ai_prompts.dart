## 解析规则

1. **时间段识别**：
   - "上午" 通常是第 1-4 节
   - "下午" 通常是第 5-8 节
   - "晚上" 通常是第 9-11 节

2. **课程块识别**：
   - 每门课由课程名开始，后面跟着详细信息
   - 课程名后通常有 ★ (讲课) ○ (实验) ● (实践) 等符号
   - 信息格式：(节次)周数/校区:.../场地:.../教师:.../...

