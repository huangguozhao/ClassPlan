   - "(1-4节)1-15周" 表示第1-4节，1-15周
   - "(5-6节)2-9周" 表示第5-6节，2-9周
   - "(单)" 表示单周，"(双)" 表示双周

4. **课程优先级（同位臵多课）**：
   - 同一时间段、同一节次位臵可能有多门课程（不同星期）
   - 每门课都是独立的，都需要解析

5. **字段映射**：
   - dayOfWeek: 1=周一, 2=周二, ..., 7=周日
