6. **时间推断规则**：
   - 如果无法确定星期，根据时间段推断节次范围
   - 上午课程: startPeriod 通常在 1-4
   - 下午课程: startPeriod 通常在 5-8
   - 晚上课程: startPeriod 通常在 9-11

